package com.company.variables;

/**
 * Javadoc a nivel de clase
 */

public class Comentarios {

    /**
     * Javadoc a nivel de método
     * @param args
     */

    public static void main(String[] args) {

        // Comentarios de una línea se escriben con dos barras
        String apellido1 = "Aparicio";

        String apellido2 = "Lara";

        /*
        Comentarios multilínea:
            para comentar varias líneas
            dentro del código
         */

        String nombre = "Jorge"; // Comentario inLine

        System.out.println(nombre + " " + apellido1 + " "  + apellido2);
    }
}
